#!/usr/bin/env node
/**
 * .env.secrets.local（マスター）から各ツール用 env を生成する。
 * 使い方: pnpm sync:env
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const masterPath = path.join(root, '.env.secrets.local');

const GENERATED = '# AUTO-GENERATED by scripts/sync-env-from-secrets.mjs — 編集しない\n# マスター: .env.secrets.local → pnpm sync:env\n\n';

function parseEnv(content) {
  const env = {};
  for (const line of content.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eq = trimmed.indexOf('=');
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq).trim();
    const value = trimmed.slice(eq + 1).trim();
    env[key] = value;
  }
  return env;
}

function formatEnvLines(keys, env) {
  return keys.map((key) => `${key}=${env[key] ?? ''}`).join('\n');
}

function write(filePath, body) {
  fs.writeFileSync(filePath, body, 'utf8');
  console.log(`updated ${path.relative(root, filePath)}`);
}

if (!fs.existsSync(masterPath)) {
  console.error('Missing .env.secrets.local — copy from .env.secrets.local.example');
  process.exit(1);
}

const env = parseEnv(fs.readFileSync(masterPath, 'utf8'));

const stripeKeys = [
  'STRIPE_PUBLISHABLE_KEY',
  'NEXT_PUBLIC_STRIPE_PUBLISHABLE_KEY',
  'STRIPE_SECRET_KEY',
  'STRIPE_WEBHOOK_SECRET',
  'STRIPE_PRICE_STARTER',
  'STRIPE_PRICE_STANDARD',
  'STRIPE_PRICE_PRO',
  'STRIPE_PRICE_LLINK_BASIC',
  'STRIPE_PRICE_LLINK_AUTO',
];

const rootEnvKeys = [
  'NEXT_PUBLIC_SUPABASE_URL',
  'NEXT_PUBLIC_SUPABASE_ANON_KEY',
  'SUPABASE_SERVICE_ROLE_KEY',
  'LINE_CHANNEL_SECRET',
  'LINE_CHANNEL_ACCESS_TOKEN',
  ...stripeKeys,
];

const appEnvKeys = [
  'NEXT_PUBLIC_SUPABASE_URL',
  'NEXT_PUBLIC_SUPABASE_ANON_KEY',
  'SUPABASE_SERVICE_ROLE_KEY',
  'APP_ENCRYPTION_KEY',
  'CRON_SECRET',
  'NEXT_PUBLIC_APP_URL',
  ...stripeKeys,
];

write(
  path.join(root, '.env.local'),
  GENERATED + formatEnvLines(rootEnvKeys, env) + '\n',
);

write(
  path.join(root, 'apps/garage-link/.env.local'),
  GENERATED + formatEnvLines(appEnvKeys, env) + '\n',
);

const lLinkEnvKeys = [
  'NEXT_PUBLIC_SUPABASE_URL',
  'NEXT_PUBLIC_SUPABASE_ANON_KEY',
  'SUPABASE_SERVICE_ROLE_KEY',
  'STRIPE_PUBLISHABLE_KEY',
  'NEXT_PUBLIC_STRIPE_PUBLISHABLE_KEY',
  'STRIPE_SECRET_KEY',
  'STRIPE_WEBHOOK_SECRET',
  'STRIPE_PRICE_LLINK_BASIC',
  'STRIPE_PRICE_LLINK_AUTO',
  'NEXT_PUBLIC_APP_URL',
];

const lLinkEnv = {
  ...env,
  NEXT_PUBLIC_APP_URL: env.NEXT_PUBLIC_L_LINK_APP_URL || env.NEXT_PUBLIC_APP_URL || 'http://localhost:3002',
};

write(
  path.join(root, 'apps/l-link/.env.local'),
  GENERATED + formatEnvLines(lLinkEnvKeys, lLinkEnv) + '\n',
);

const claudeEnv = {};
for (const key of stripeKeys) {
  claudeEnv[key] = env[key] ?? '';
}
write(
  path.join(root, '.claude/settings.local.json'),
  JSON.stringify({ env: claudeEnv }, null, 2) + '\n',
);

const codexToml = `${GENERATED.replace(/^# /gm, '# ')}[shell_environment_policy]
inherit = "core"
include_only = [
  "PATH",
  "HOME",
  ${stripeKeys.map((k) => `"${k}"`).join(',\n  ')},
]

[shell_environment_policy.set]
${stripeKeys.map((k) => `${k} = "${(env[k] ?? '').replace(/"/g, '\\"')}"`).join('\n')}
`;

write(path.join(root, '.codex/config.toml'), codexToml);

console.log('Done. Master: .env.secrets.local');
